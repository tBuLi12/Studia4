package com.papz22.studia4.repository;

public class PollResult {
    private int slotId;
    private int rating;

    public PollResult() {
    }

    public int getSlotId() {
        return this.slotId;
    }

    public void setSlotId(int slotId) {
        this.slotId = slotId;
    }

    public int getRating() {
        return this.rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

}
